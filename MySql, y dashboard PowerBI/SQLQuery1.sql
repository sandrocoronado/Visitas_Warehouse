CREATE TABLE VISITORS (
  visitor_id INT PRIMARY KEY IDENTITY(1,1),
  first_name NVARCHAR(50),
  last_name NVARCHAR(50),
  country NVARCHAR(50),
  email NVARCHAR(100),
  phone NVARCHAR(20),
  gender NVARCHAR(1),
);

CREATE TABLE WEBSITES (
  website_id INT PRIMARY KEY IDENTITY(1,1),
  url1 NVARCHAR(255),
  category NVARCHAR(100),
  name1 NVARCHAR(100)
);

CREATE TABLE REFERERS (
    referer_id INT PRIMARY KEY IDENTITY(1,1),
	referer NVARCHAR(50),
    referer_url VARCHAR(255) NOT NULL
);

CREATE TABLE PAGEVIEWS (
  pageview_id INT PRIMARY KEY IDENTITY(1,1),
  visitor_id INT FOREIGN KEY REFERENCES VISITORS(visitor_id),
  website_id INT FOREIGN KEY REFERENCES WEBSITES(website_id),
  referer_id INT FOREIGN KEY REFERENCES REFERERS(referer_id),
  timestamp1 DATETIME,
);
